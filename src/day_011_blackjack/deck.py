from random import shuffle
from typing import Union


########################################################################################################################

class Deck52Standard:
    def __init__(self):
        """

        """

        self._deck = []
        self._reset()

########################################################################################################################

    def _reset(self) -> None:
        """

        """

        self._deck = 4 * (list(range(2, 12)) + 3 * [10])

########################################################################################################################

    def shuffle(self) -> None:
        """

        """

        self._reset()
        shuffle(self._deck)

########################################################################################################################

    def draw(self) -> Union[int, None]:
        """

        :return:
        """

        if len(self._deck) > 0:
            card = self._deck[0]
            self._deck = self._deck[1:]
            return card

